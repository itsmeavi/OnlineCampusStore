package dto;

/**
 *
 * @author free_bird
 */
public class Professor {
    private String profId;
    private String profName;
    private String profDegree;
    private String profSpecialization;
    private String undergoingProjects;
    private String teachingSubjects;
    private String deptId;

    /**
     * @return the profId
     */
    public String getProfId() {
        return profId;
    }

    /**
     * @param profId the profId to set
     */
    public void setProfId(String profId) {
        this.profId = profId;
    }

    /**
     * @return the profName
     */
    public String getProfName() {
        return profName;
    }

    /**
     * @param profName the profName to set
     */
    public void setProfName(String profName) {
        this.profName = profName;
    }

    /**
     * @return the profDegree
     */
    public String getProfDegree() {
        return profDegree;
    }

    /**
     * @param profDegree the profDegree to set
     */
    public void setProfDegree(String profDegree) {
        this.profDegree = profDegree;
    }

    /**
     * @return the profSpecialization
     */
    public String getProfSpecialization() {
        return profSpecialization;
    }

    /**
     * @param profSpecialization the profSpecialization to set
     */
    public void setProfSpecialization(String profSpecialization) {
        this.profSpecialization = profSpecialization;
    }

    /**
     * @return the undergoingProjects
     */
    public String getUndergoingProjects() {
        return undergoingProjects;
    }

    /**
     * @param undergoingProjects the undergoingProjects to set
     */
    public void setUndergoingProjects(String undergoingProjects) {
        this.undergoingProjects = undergoingProjects;
    }

    /**
     * @return the teachingSubjects
     */
    public String getTeachingSubjects() {
        return teachingSubjects;
    }

    /**
     * @param teachingSubjects the teachingSubjects to set
     */
    public void setTeachingSubjects(String teachingSubjects) {
        this.teachingSubjects = teachingSubjects;
    }

    /**
     * @return the deptId
     */
    public String getDeptId() {
        return deptId;
    }

    /**
     * @param deptId the deptId to set
     */
    public void setDeptId(String deptId) {
        this.deptId = deptId;
    }

    
}
